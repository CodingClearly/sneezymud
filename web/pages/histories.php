The Histories of <b>SneezyMUD</b>:</p>

<p align="left">
The SneezyMUD Histories section contains background information on the lands
and people you will find int The World of Grimhaven.  Here you can access
<a href="index.php?page=grimoire">the Grand Grimore</a>, the definative history
on the creation of The World and the races that people it.  From these pages you
may also view the tomes that describe the various <a href="index.php?page=factionhistories">
faction histories</a>, detailing the background of the political and theological parties
of SneezyMUD.  Also to be found in this section are the chronicles of 
<a href="index.php?page=quests">adventures past</a>, detailed first-hand accounts by the 
travellers of our world.
