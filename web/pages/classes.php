      </td>
    </tr>

    <tr>
      <td width="16%" align="right" valign="top"><b>Shaman&nbsp;&nbsp;</b></td>
      <td width="84%" valign="top">
        Shamans are masters of the undead and other natural magics.  Unlike other casters, combat skills are a part of their natural development.  Also unlike other casters, they rely on an ability to channel life force, and can die if they use up their store.
      </td>
    </tr>

