      <td width="84%" valign="top">
        Thieves are the unseen robbers and killers of The World.  A very powerful offensive class.
      </td>
    </tr>

    <tr>
      <td width="16%" align="right" valign="top"><b>Warrior&nbsp;&nbsp;</b></td>
      <td width="84%" valign="top">
        Warriors are the bread and butter fighters of The World.  They are powerful opponents when well equipped, but can be difficult to play without companions, as their combat styles often require them to absorb a lot of damage, or, at high level, to sacrifice offensive power to make them into defensive monoliths.
