<p align="center">About <B>SneezyMUD</b>:</p>
<p align="left">SneezyMUD has been in production for over ten years to date, and continues to be
one of the most advanced and entertaining MUDs on the internet.  The original SneezyMUD was a
stock SillyMUD base (a derivative of the famous <a class="nav" href="http://dikumud.com/">Diku</a> code base) aquired in 1992 by SneezyMUD's founder, <a class="nav" href="index.php?page=immortals&which=brutius">Brutius</a>.
Since then, we've been through numerous major code revamps, including two complete code rewrites.
Now, SneezyMUD is completely coded in C++ (Diku and SillyMUD were both C bases) and is completely
object oriented.  In addition, we now use databases for most of our data storage, moving away from
the massive text files of days long gone.</p>
<p align="left">Apart from our advances in basic MUD infrastructure, SneezyMUD has and will always
will be devoted to providing an in-depth and innovative gaming experience, and is constantly adding
new features that add to the fun and balance of the game.</p>
<p align="left">The direction of SneezyMUD is under the control of the <a class="nav" href="index.php?page=mudadmin">MUDAdmin</a>, a council of higher
level immortals which make executive decisions concerning the MUD.  The outcome of some MUDAdmin votes are
made available publicly on this site.
</p>
